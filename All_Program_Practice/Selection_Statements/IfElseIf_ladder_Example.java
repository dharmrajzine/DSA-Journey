public class IfElseIf_ladder_Example {

	public static void main(String[] args) {

		int i = 20;

		if (i == 10)
			System.out.println("i is 10\n");

		else if (i == 15)
			System.out.println("i is 15\n");

		if (i == 20)
			System.out.println("i is 20\n");

		System.out.println("Outside if-else-if");
	}
}
